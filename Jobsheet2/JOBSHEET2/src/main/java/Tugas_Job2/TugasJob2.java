public class TugasJob2 {
    public static void main(String[] args) {
        // Dibawah ini adalah komentar
        String nama = "Dhaffa";
        String Nama = "Noflian";
        
        // Variabel "nama" dan "Nama" berbeda, karena Java bersifat case sensitive
        System.out.println("Hai! Saya " + nama + " " + Nama);
        
        // Perbedaan besar-kecil huruf juga berlaku untuk metode seperti di bawah ini
        // syntax ".length()" untuk mengetahui panjang dari suatu String
        System.out.println("\nPanjang nama: " + nama.length());
        // isi dari "nama" adalah Dhaffa
        System.out.println("Panjang Nama: " + Nama.length());  
        // isi dari "Nama" adalah Noflian
    }
}
